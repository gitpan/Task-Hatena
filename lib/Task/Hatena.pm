=head1 NAME

Task::Hatena - installs all the Hatena modules

=head1 VERSION

Task::Hatena 0.01

=head1 SYNOPSIS

  cpan Task::Hatena

=head1 DESCRIPTION

This module gathers together all the Hatena modules in one convenient place
in order to satisfy the requirements of other modules that use Hatena such 
as Apache, Kwiki, Catalyst, etc.

